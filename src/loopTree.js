function getMenuItem(menuData, id) {
    if (!menuData || menuData.length === 0) {
        return null;
    }
    for(const item of menuData){
       let result = loopTree(item, id);
       if(result){
           return result;
       }
    }
    return null;
}




function loopTree(treeData, id){
    if(id === treeData.id){
        const {children, ...rest} = treeData;
        return {...rest};
    }else if(!treeData.children){
        return null;
    }else{
        for(const child of treeData.children){
            let result = loopTree(child, id);
            if(result){
                const {children, ...rest} = result;
                return {...rest};
            }
        }
        return null;       
    }    
}




